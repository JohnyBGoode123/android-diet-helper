SELECT Диеты.Название,
СписокВидовДиет.Название AS ВидДиеты,
Рекомендации_к_диетам.Вспомогательная_информация,
"Рекомендации_к_диетам"."Нежелательные продукты",
Рекомендации_к_диетам.Рекомендуемые_продукты,
Диеты.Длительность
FROM Диеты INNER JOIN СписокВидовДиет
ON Диеты.Вид_Диеты = СписокВидовДиет.ID
INNER JOIN Рекомендации_к_диетам
ON Диеты.Рекомендации = Рекомендации_к_диетам.Id_диеты
WHERE Диеты.Название = "Гречневая"
